"""Logging helpers for the dataset processing pipeline."""
from __future__ import annotations

import logging
from pathlib import Path


def configure_logging(log_file: Path, level: str = "INFO") -> logging.Logger:
    """Configure root logging handlers for console + file output."""
    log_file = Path(log_file)
    log_file.parent.mkdir(parents=True, exist_ok=True)

    logging.basicConfig(
        level=getattr(logging, level.upper(), logging.INFO),
        format="%(message)s",
        handlers=[
            logging.FileHandler(log_file, mode="w", encoding="utf-8"),
            logging.StreamHandler()
        ]
    )
    return logging.getLogger("dataset_pipeline")

